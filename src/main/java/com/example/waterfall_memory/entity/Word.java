package com.example.waterfall_memory.entity;

public class Word {
	private String wId;
	private String gqs;
	private String gqfc;
	private String xzfc;
	private String fs;
	private String meaning;
	private String lx;

	public Word() {
		super();
	}

	public String getGqs() {
		return gqs;
	}
	public void setGqs(String gqs) {
		this.gqs = gqs;
	}
	public String getGqfc() {
		return gqfc;
	}
	public void setGqfc(String gqfc) {
		this.gqfc = gqfc;
	}
	public String getXzfc() {
		return xzfc;
	}
	public void setXzfc(String xzfc) {
		this.xzfc = xzfc;
	}
	public String getFs() {
		return fs;
	}
	public void setFs(String fs) {
		this.fs = fs;
	}
	public String getMeaning() {
		return meaning;
	}
	public void setMeaning(String meaning) {
		this.meaning = meaning;
	}
	public String getLx() {
		return lx;
	}
	public void setLx(String lx) {
		this.lx = lx;
	}

	public String getwId() {
		return wId;
	}

	public void setwId(String wId) {
		this.wId = wId;
	}
	

}
